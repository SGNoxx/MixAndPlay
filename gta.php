                    <p class="border mi">"GTA 5" propose une bande-son éclectique comprenant une grande variété de genres musicaux. Voici quelques-uns des genres musicaux représentés dans le jeu :
                                         Hip-hop : Le hip-hop est l'un des genres dominants dans la bande-son de "GTA 5". Vous pouvez entendre des artistes comme Kendrick Lamar, A$AP Rocky, et Tyler, The Creator.
                                     Rock : Le rock est également présent avec des titres de Queen, Bob Seger, et The Black Keys, entre autres.
                                Électronique : Le jeu comprend une sélection de musique électronique avec des artistes tels que Flying Lotus, Solomun, et Dixon.
                            Pop : Des chansons pop sont également présentes dans le jeu, avec des artistes comme Britney Spears, Rihanna, et Maroon 5.
                        Reggae : On trouve aussi des morceaux de reggae, avec des artistes comme Bob Marley et Jimmy Cliff.</p>
