                        <p>
                            1- Hip-hop : Genre musical issu des quartiers urbains, caractérisé par des rythmes percussifs, des paroles poétiques et des techniques de rap. Il aborde souvent des thèmes tels que la vie urbaine, les défis sociaux et les expériences personnelles.
                            2- Rock : Genre musical qui met l'accent sur les guitares électriques, la batterie et la basse, avec des racines dans le blues et le rock 'n' roll. Il comporte une large gamme de sous-genres, allant du rock classique au hard rock, en passant par le punk rock et le heavy metal.
                            3- Électronique : Genre musical basé sur l'utilisation de synthétiseurs, de boîtes à rythmes et d'autres équipements électroniques pour créer des compositions sonores. Il englobe une variété de styles, notamment la musique techno, la house, la trance et l'ambient.
                            4- Pop : Genre musical populaire caractérisé par des mélodies accrocheuses, des structures simples et des arrangements accessibles. Il est souvent axé sur la production et la commercialisation, avec une large diffusion à la radio et dans les médias grand public.
                            5- Reggae : Genre musical originaire de la Jamaïque, mettant l'accent sur le rythme de type skank, les accents syncopés et les paroles socio-politiques ou spirituelles. Il incorpore souvent des éléments de soul, de rhythm and blues et de ska.</p>
