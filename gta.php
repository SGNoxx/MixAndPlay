                        <p>"GTA 5" est un jeu d'action-aventure en monde ouvert où les joueurs contrôlent trois personnages principaux, Michael, Franklin et Trevor, alors qu'ils commettent divers crimes dans la ville fictive de Los Santos, une version satirique de Los Angeles. Le jeu offre une histoire riche, un vaste monde ouvert à explorer, des graphismes impressionnants et une grande variété d'activités criminelles et récréatives.</p>
